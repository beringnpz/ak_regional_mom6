    'maskval', [1 2 7], ...
    'Cdata', Cdata);

masklbls = ["allESR", "BS", "AI", "GOA",  "NBS", "SEBS", "AItob", "AItobsvy", "AItobdeep", "AItos"];

for ii = 5:10
    fname = fullfile(esrfol, sprintf('clustering_0701-0822_%s',masklbls{ii}));
    if ~exist([fname '.png'], 'file')
        export_fig(fname, h(ii).fig, '-png', '-r150');
    end
