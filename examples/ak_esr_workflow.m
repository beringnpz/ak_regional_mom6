%% Data processing

%% ... Extract simulation data (run on PPAN)
%
%  First, run ak_esr_peec_extract.sh to get new and/or updated data.
%  Update these variables to match the release details and location of the
%  extracted data.

% Local "CEFI Portal" options

C = cefiportalopts('portalpath', '~/Documents/Data/CEFI/ak_cefiportal', ...
                   'release', 'e202507', ...
                   'subdomain', [0 342 446 743], ...
                   'yyyymmdd', '20240101');

yr = 2025;
esrfol = '~/Documents/Manuscripts/2025_ESR/';

%% ... Create masking variables file

[Scs, Mask] = build_ak_masks(...
    'cpopts', C.setopts('freq','static'), ...
    'expandname', true, ....
    'setuponly', true, ...
    'maskonly', true, ...
    'newname', 'ak_masks');

%% ESR Plots

%% ... Map of bottom temperature, past 20 years

fname = fullfile(esrfol, sprintf('btemp_map_%d-%d', [yr-20+1 yr]));

h = plot_bering_bottom_temp_map_5x4(yr, C);

if ~exist([fname '.png'], 'file')
    export_fig(fname, h.fig, '-png', '-r150', '-nocrop');
end

%% Cluster analysis: July 1 surf/bottom temp anomalies from 6/1-8/22

% 3 primary ESR regions

[hclus(1),Cdata] = cluster_on_anomalies(...
    [6 1], ...
    'cpopts', C, ...
    'yr', 1993:yr, ...
    'ndays', 82);

% Bering

hclus(2) = cluster_on_anomalies(...
    [6 1], ...
    'cpopts', C, ...
    'yr', 1993:yr, ...
    'maskval', 3, ...
    'Cdata', Cdata, ...
    'ndays', 82);

% Aleutians

hclus(3) = cluster_on_anomalies(...
    [6 1], ...
    'cpopts', C, ...
    'yr', 1993:yr, ...
    'maskval', 1, ...
    'Cdata', Cdata, ...
    'ndays', 82);

% Gulf of Alaska

hclus(4) = cluster_on_anomalies(...
    [6 1], ...
    'cpopts', C, ...
    'yr', 1993:yr, ...
    'maskval', 4, ...
    'Cdata', Cdata, ...
    'ndays', 82);

masklbls = ["allESR", "BS", "AI", "GOA"];

for ii = 1:4
    fname = fullfile(esrfol, sprintf('clustering_0701-0822_%s',masklbls{ii}));
    if ~exist([fname '.png'], 'file')
        export_fig(fname, hclus(ii).fig, '-png', '-r150');
    end
end

%% ... extra variations

% NBS vs SEBS

hclus(5) = cluster_on_anomalies( ...
